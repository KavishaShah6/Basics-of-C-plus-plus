#include <bits/stdc++.h>
using namespace std;

int main () { 
    // Initialization at Declaration
    int arr1[5] = {1, 2, 3, 4, 5};
    int arr2[] = {10, 20, 30, 40};
    int arr3[] = {2, 4, 6, 8};

    // Accessing array elements (index starts from 0)
    cout << "The fourth element of first array: " << arr1[3] << endl;
    cout << "The third element of second array: " << arr2[2] << endl;
    cout << "The third element of third array: " << arr3[2] << endl;


    return 0;
}
